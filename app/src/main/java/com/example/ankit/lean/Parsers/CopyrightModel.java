
class CopyrightModel {

	public String logo;
	public String licence_url;
	public String holder_url;
	public int year;
	public String licence;
	public String holder;

	public CopyrightModel(String logo, String licence_url, String holder_url, int year, String licence, String holder) {

		this.logo = logo;
		this.licence_url = licence_url;
		this.holder_url = holder_url;
		this.year = year;
		this.licence = licence;
		this.holder = holder;

	}

}